fun main(){
    var a =5
   println(++a) // Yha value increment ho kr incremented print hoga
   println("printing the value of a  :- $a") 
   println(a++) // Yha value increment ho kr without incremented print hoga
               // Next khi v a ke value ko use krege to incremented value use hoga 
   println("printing the value of a  :- $a") 

   var b =5
   println(--b) // Yha value decrement ho kr incremented print hoga
   println("printing the value of b  :- $b") 
   println(b--) // Yha value decrement ho kr without decremented print hoga
               // Next khi v b ke value ko use krege to decremented value use hoga 
   println("printing the value of b  :- $b") 

}