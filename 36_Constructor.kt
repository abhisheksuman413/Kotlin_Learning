/*
Constructor >>>
                In Kotlin, constructors are used to initialize objects
                when you create instances of a class. Kotlin provides 
                two types of constructors:

            >>> Primary Constructor: This is defined in the class header 
                itself and can have parameters. You can only have one 
                primary constructor in a class.

            >>> Secondary Constructor: These are defined inside the class 
                body and can have zero or more parameters. You can have 
                multiple secondary constructors in a class.

Init Block:- The init block is used for additional initialization 
             logic within the class. It's executed when an object 
             is created after the primary constructor has been 
             called.




*/